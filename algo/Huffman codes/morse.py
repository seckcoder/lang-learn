def translateToMorseCode(sentence):
    sentence = ' '.join(sentence.split())
    morse_code = ''
# space between letters is one unit
# space between words is two units
    for s in sentence.upper():
        if s == ' ':
            morse_code += '  '
        if MorseCode.has_key(s):
            morse_code += MorseCode[s]
            morse_code += ' '

    return morse_code 
